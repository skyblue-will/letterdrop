// Get today's puzzle number
export function getPuzzleNumber(): number {
  const startDate = new Date('2026-02-02').getTime(); // Launch day
  const today = new Date().setHours(0, 0, 0, 0);
  return Math.floor((today - startDate) / (1000 * 60 * 60 * 24)) + 1;
}

// Get deterministic words for a puzzle number (5 words per puzzle)
export function getWordsForPuzzle(puzzleNumber: number): string[] {
  const words: string[] = [];
  const seed = puzzleNumber * 12345;
  
  for (let i = 0; i < 5; i++) {
    const index = (seed + i * 7919) % WORDS.length;
    words.push(WORDS[index].toUpperCase());
  }
  
  return words;
}

// Get random word for practice mode
export function getRandomWord(): string {
  const index = Math.floor(Math.random() * WORDS.length);
  return WORDS[index].toUpperCase();
}
